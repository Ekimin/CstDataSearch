package com.jinke.cstsearch.model;

public class CstContract {
    private String cstName;
    private String cardId;
    private String BldArea;
    private String TnArea;
    private String BldCjPrice;
    private String TnCjPrice;
    private String HtTotal;
    private String SjBcTotal;
    private String ContractDate;
    private String JFDate;
    private String qyyxType; //全员营销类型

    public String getCstName() {
        return cstName;
    }

    public void setCstName(String cstName) {
        this.cstName = cstName;
    }

    public String getCardId() {
        return cardId;
    }

    public void setCardId(String cardId) {
        this.cardId = cardId;
    }

    public String getBldArea() {
        return BldArea;
    }

    public void setBldArea(String bldArea) {
        BldArea = bldArea;
    }

    public String getTnArea() {
        return TnArea;
    }

    public void setTnArea(String tnArea) {
        TnArea = tnArea;
    }

    public String getBldCjPrice() {
        return BldCjPrice;
    }

    public void setBldCjPrice(String bldCjPrice) {
        BldCjPrice = bldCjPrice;
    }

    public String getTnCjPrice() {
        return TnCjPrice;
    }

    public void setTnCjPrice(String tnCjPrice) {
        TnCjPrice = tnCjPrice;
    }

    public String getHtTotal() {
        return HtTotal;
    }

    public void setHtTotal(String htTotal) {
        HtTotal = htTotal;
    }

    public String getSjBcTotal() {
        return SjBcTotal;
    }

    public void setSjBcTotal(String sjBcTotal) {
        SjBcTotal = sjBcTotal;
    }

    public String getContractDate() {
        return ContractDate;
    }

    public void setContractDate(String contractDate) {
        ContractDate = contractDate;
    }

    public String getJFDate() {
        return JFDate;
    }

    public void setJFDate(String JFDate) {
        this.JFDate = JFDate;
    }

    public String getQyyxType() {
        return qyyxType;
    }

    public void setQyyxType(String qyyxType) {
        this.qyyxType = qyyxType;
    }
}
